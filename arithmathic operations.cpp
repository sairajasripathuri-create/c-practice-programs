#include <stdio.h>

int main() {
    int num1, num2;

    // Read the two integers
    scanf("%d %d", &num1, &num2);

    // Perform and print arithmetic operations
    printf("Sum:%d
", num1 + num2);
    printf("Difference:%d
", num1 - num2);
    printf("Product:%d
", num1 * num2);
    printf("Quotient:%d
", num1 / num2);
    printf("Remainder:%d
", num1 % num2);

    return 0;
}


